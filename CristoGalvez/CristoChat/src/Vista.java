    private void lista_amigosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lista_amigosMouseClicked
        
        this.user_dest = lista_amigos.getSelectedValue();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        
        String[] spliteo = user_dest.split(" ");
        usuario_seleccionado = spliteo[0];
        controladora.generateStringForDataUser(spliteo[0]);
        controladora.cadenaPedirFoto(spliteo[0]);
        controladora.protocolocliente.select_user=spliteo[0];
        controladora.generateStringForMessage(user, spliteo[0], sdf.format(timestamp));
        

      
    }//GEN-LAST:event_lista_amigosMouseClicked

